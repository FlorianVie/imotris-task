            <h4>But de l'étude</h4>
            <p>Adaptation en français et validation psychométrique du questionnaire IMOTRIS (Lyons & Guznov 2019).</p>

            <h4>Engagement du participant</h4>
            <p>L’étude va consister à répondre à des questionnaires sur votre ressenti vis-à-vis de la technologie et à réaliser une tâche informatisée de préparation culinaire simplifiée. Cette expérimentation ne présente aucun risque pour le participant, autant sur la dimension physique que morale et sociale.</p>

            <h4>Engagement de l’expérimentateur principal</h4>
            <p>En tant qu’expérimentateur principal, il s’engage à mener cette recherche selon les dispositions éthiques et déontologiques, à protéger l’intégrité physique, psychologique et sociale des personnes tout au long de l'expérience et à assurer la confidentialité des informations recueillies. </p>

            <h4>Liberté du participant</h4>
            <p>Le consentement pour poursuivre l'expérience peut être retiré à tout moment sans donner de raison et sans encourir aucune responsabilité ni conséquence. Pour ce faire, il suffit au participant de fermer la fenêtre de son navigateur internet dans laquelle se trouve l’expérience. Les données recueillies ne seront transmises à l’expérimentateur principal qu’une fois l’expérience complétement terminée.</p>

            <h4>Information du participant</h4>
            <p>Le participant a la possibilité d’obtenir des informations supplémentaires concernant cette étude auprès de l’expérimentateur principal, et ce, dans les limites des contraintes du plan de recherche.</p>

            <h4>Confidentialité des informations</h4>
            <p>Toutes les informations concernant les participants seront conservées de façon anonyme et confidentielle. Aucune information à caractère personnelles sera recueillie durant cette étude, seules les réponses aux questionnaires, la performance à la tâche et le support informatique utilisé seront sauvegardés. Toutefois, conformément à la loi informatique et libertés, les participants sont en droit de réclamer la restitution ou la suppression des données les concernant. Pour ce faire, tout en gardant l’anonymat, le participant devra conserver un identifiant généré aléatoirement, il permet à l’expérimentateur de supprimer les données ou transmettre les données du participant lorsque ce dernier lui en fait la demande.</p>
            <p>Les données sont stockées dans une base de données administrée par l’expérimentateur principal de l’étude et pourront être réutilisées dans le cadre de publications scientifiques. Toutes les données, qui ne font pas l’objet d’une demande de suppression, seront conservées pour une durée de 5 ans à compter de la date du début de l’étude.</p>
            <p>Cette recherche n’ayant qu’un caractère psychologique, elle n’entre pas de ce fait dans la loi Huriet-Sérusclat concernant la protection des personnes dans la recherche biomédicale. La transmission des informations concernant le participant pour l’expertise ou pour la publication scientifique sera, elle aussi, anonyme.</p>

            <h4>Déontologie et éthique</h4>
            <p>Le promoteur et l’expérimentateur principal s’engagent à préserver absolument la confidentialité et le secret professionnel pour toutes les informations concernant le participant (titre I, articles 1,3,5 et 6 et titre II, articles 3, 9 et 20 du code de déontologie des psychologues, France).</p>

